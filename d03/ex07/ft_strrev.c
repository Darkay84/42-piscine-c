char	*ft_strrev(char *str)
{
    int i;
    int j;
    char swp;
    
    i = 0;
    j = 0;
    while (str[j])
    {
        j++;
    }
    j--;
    while (i < j)
    {
        swp = str[j];
        str[j] = str[i];
        str[i] = swp;
        j++;
    }
    return (str);
}
