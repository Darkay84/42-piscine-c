char	*ft_strrev(char *str)
{
	char	swp;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[j])
	{
		j++;
	}
	while (i < j / 2)
	{
		swp = str[i];
		str[i] = str[j - i - 1];
		str[j - i - 1] = swp;
		i++;
	}
	return (str);
}
