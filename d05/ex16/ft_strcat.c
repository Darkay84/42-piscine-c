char *ft_strcat(char *dest, char *src)
{
    char *c;
    char *d;
    
    c = dest;
    d = (char *)src;

    while (*c != '\0')
        c++;
    while (*d != '\0')
    {
        *c= *d;
        c++;
        d++;
    }
    *c = '\0';
    return (dest);
}
