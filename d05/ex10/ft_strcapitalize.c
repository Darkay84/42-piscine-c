char	*ft_strcapitalize(char *str)
{
	int i;

	if (str[0] > 96 && str[0] < 123)
		str[i] -= 32;
	i = 1;
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
			str[i] += 32;
		i++;
	}
	i = 1;
	while (str[i] != '\0')
	{
		if ((str[i - 1] > 122 || str[i - 1] < 97)
				&& (str[i - 1] > 90 || str[i - 1] < 65) && (str[i - 1] > 57
				|| str[i - 1] < 48) && (str[i] < 123 && str[i] > 96))
			str[i] -= 32;
		else if ((str[i - 1] > 122 || str[i - 1] < 97)
				&& str[i] > 64 && str[i] < 91)
			str[i] += 32;
		i++;
	}
	return (str);
}
