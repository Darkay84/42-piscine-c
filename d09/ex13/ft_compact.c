int	ft_compact(char **tab, int length)
{
	int i;
	int j;

	i = 0;
	while (*tab[i])
	{
		if (*tab[i] == 0)
		{
			j = 0;
			while (*tab[j + 1] != '\0')
			{
				*tab[j] = *tab[j + 1];
				j++;
			}
			*tab[j] = *tab[j + 1];
		}
		i++;
	}
	length = i;
	return (length);
}
